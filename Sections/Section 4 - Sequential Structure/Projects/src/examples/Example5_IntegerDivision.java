package examples;

import java.util.Locale;

public class Example5_IntegerDivision {

	public static void main(String[] args) {

		Locale.setDefault(Locale.US);

		// Variables Declaration
		int a, b;
		double result;

		// Variables Assignment
		a = 5;
		b = 2;

		// Processing
		result = a / b;

		// Data Output
		System.out.println(result); // 2.0

	}

}
