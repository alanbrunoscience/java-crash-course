package b.dataprocessing;

public class Example6_Casting2 {

	public static void main(String[] args) {

		// Variables Declaration
		int a, b;
		double result;

		// Variables Assignment
		a = 5;
		b = 2;

		// Processing
		result = (double) a / b;

		// Data Output
		System.out.println(result); // 2.5

	}

}
