package dataprocessing;

public class Example2_IntDoubleMultiplication {

	public static void main(String[] args) {

		// Variables Declaration
		int x;
		double y;

		// Variables Assignment
		x = 5;
		y = 2 * x;

		// Data Output
		System.out.println(x); // 5
		System.out.println(y); // 10.0

	}

}
