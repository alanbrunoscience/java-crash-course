/**
 * Ternary Conditional Expression
 * 
 * An optional structure for if-else when we want to decide a value based on a condition.
 * 
 * Syntax:
 * 
 * ( condition ) ? value_if_true : value_if_false;
 * 
 */

package f.ternaryconditionalexpression;

public class Example1_Explanation {

	public static void main(String[] args) {

		System.out.println((2 > 4) ? 50 : 80);

		System.out.println((10 != 3) ? "Maria" : "Alex");

	}

}
