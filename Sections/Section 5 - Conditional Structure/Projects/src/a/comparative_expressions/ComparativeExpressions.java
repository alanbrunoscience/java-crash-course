package a.comparative_expressions;

public class ComparativeExpressions {

	public static void main(String[] args) {
		
		// Variable declaration
		int x = 5;
		
		// Data output
		System.out.println("*** Comparative Expressions ***\n");
		System.out.printf("-> x = %d%n%n", x); // -> x = 5
		System.out.printf("1) x > 0 -> %s;%n", x > 0); // 1) x > 0 -> true;
		System.out.printf("2) x == 3 -> %s;%n", x == 3); // 2) x == 3 -> false;
		System.out.printf("3) 10 <= 30 -> %s;%n", 10 <= 30); // 3) 10 <= 30 -> true;
		System.out.printf("4) x != 2 -> %s.%n", x != 2); // 4) x != 2 -> true.

	}

}
