package a.comparativeexpressions;

public class ComparativeExpressions {

	public static void main(String[] args) {
		
		// Variable declaration
		int x = 5;
		
		// Data output
		System.out.println("*** Comparative Expressions ***\n");
		System.out.printf("-> x = %d%n%n", x);
		System.out.printf("1) x > 0 -> %s;%n", x > 0);
		System.out.printf("2) x == 3 -> %s;%n", x == 3);
		System.out.printf("3) 10 <= 30 -> %s;%n", 10 <= 30);
		System.out.printf("4) x != 2 -> %s.%n", x != 2);

	}

}
